#Lista são usadas para armazenar varios itens numa varíavel só. São usadas [] para criar a lista. podem armazenar qualquer tipo de dados, conforme o exemplo abaixo
gameList = ['Fifa 23', 2023, 90.50, True]
print(gameList)

gameList = ['Residente Evil 4', 'Star Wars', 'Call of Duty 3']
print(gameList)

#Buscar os dois primeiros itens da lista

print(gameList[:2])

#Buscar o último item da lista

print(gameList[-1])

#Buscar até o último item da lista

print(gameList[:3])

#Buscar de uma posição em diante

print(gameList[1:3])